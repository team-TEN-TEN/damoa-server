-- VIEW 이력 생성
WITH RECURSIVE seq(n) AS (
    SELECT 1
    UNION ALL
    SELECT n + 1 FROM seq WHERE n < 100
)
INSERT INTO interaction_history (category, created_at, post_id)
SELECT 'VIEW',
       TIMESTAMPADD(SECOND, FLOOR(RAND() * TIMESTAMPDIFF(SECOND, p.created_at, '2024-08-31 23:59:59')), p.created_at),
       p.id
FROM post p
         JOIN seq ON seq.n <= p.view_count
ORDER BY p.id, seq.n;

-- LIKE 이력 생성
WITH RECURSIVE seq(n) AS (
    SELECT 1
    UNION ALL
    SELECT n + 1 FROM seq WHERE n < 100
)
INSERT INTO interaction_history (category, created_at, post_id)
SELECT 'LIKE',
       TIMESTAMPADD(SECOND, FLOOR(RAND() * TIMESTAMPDIFF(SECOND, p.created_at, '2024-08-31 23:59:59')), p.created_at),
       p.id
FROM post p
         JOIN seq ON seq.n <= p.like_count
ORDER BY p.id, seq.n;

-- SHARE 이력 생성
WITH RECURSIVE seq(n) AS (
    SELECT 1
    UNION ALL
    SELECT n + 1 FROM seq WHERE n < 100
)
INSERT INTO interaction_history (category, created_at, post_id)
SELECT 'SHARE',
       TIMESTAMPADD(SECOND, FLOOR(RAND() * TIMESTAMPDIFF(SECOND, p.created_at, '2024-08-31 23:59:59')), p.created_at),
       p.id
FROM post p
         JOIN seq ON seq.n <= p.share_count
ORDER BY p.id, seq.n;


